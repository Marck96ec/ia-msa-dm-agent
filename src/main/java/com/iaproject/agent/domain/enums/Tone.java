package com.iaproject.agent.domain.enums;

/**
 * Tono conversacional del usuario.
 * Define la personalidad y estilo emocional de las respuestas de la IA.
 */
public enum Tone {
    /**
     * Tono cálido y humano, empático y cercano.
     * Usa lenguaje acogedor y muestra comprensión emocional.
     */
    WARM,
    
    /**
     * Tono neutral, profesional y objetivo.
     * Lenguaje claro sin carga emocional excesiva.
     */
    NEUTRAL,
    
    /**
     * Tono formal, cortés y distante.
     * Lenguaje elegante y respetuoso, adecuado para contextos corporativos.
     */
    FORMAL,
    
    /**
     * Tono divertido y relajado.
     * Incluye humor inteligente y lenguaje informal cuando es apropiado.
     */
    FUNNY
}
